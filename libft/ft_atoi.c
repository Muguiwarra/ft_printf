#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	int				m;
	unsigned long	nb;

	i = 0;
	m = 1;
	nb = 0;
	while (((str[i] >= 9 && str[i] <= 13) || str[i] == 32))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			m = -1;
		i++;
	}
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
	{
		nb = nb * 10 + str[i] - '0';
		if (nb > 9223372036854775807 && m == 1)
			return (-1);
		if (nb > 9223372036854775807 && m == -1)
			return (0);
		i++;
	}
	return ((int)(nb * m));
}
